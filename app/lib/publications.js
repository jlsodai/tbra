export const filterPublicationYear = (year) => {
	return publications.filter(
		(publication) => publication.publicationYears.nodes[0].name == year
	);
};

export const sortedPublications = publications.sort((first, next) =>
	first.publicationYears.nodes[0].name < next.publicationYears.nodes[0].name
		? 1
		: -1
);
